function setLoading() {
  resultBox.innerHTML = '<span class="loading">Thinking…</span>';
}
function setError(message) {
  resultBox.innerHTML = `<span class="error-text">${message}</span>`;
}
function setResult(text) {
  resultBox.textContent = text;
}

async function askStudentWordHelp(questionText) {
  try {
    setLoading();
    const data = await window.postJson(`${window.APP_CONFIG.API_BASE}/ai`, {
      course: window.APP_CONFIG.COURSE,
      lesson: window.APP_CONFIG.LESSON,
      task: 'student_word_help',
      question: questionText,
      context: 'Lesson 3.4 discusses two idiom stories, 天衣无缝 and 胸有成竹, with pattern learning around 身字旁, 门字框, 酉-related words, and story-based idiom memory.'
    });
    setResult(data.result || 'No response returned.');
  } catch (error) {
    setError(error.message || 'Something went wrong.');
  }
}

