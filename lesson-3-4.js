function bindConceptChips() {
  document.querySelectorAll('.concept-chip-wrap').forEach((wrap) => {
    const chip = wrap.querySelector('.concept-chip');
    const tooltip = wrap.querySelector('.concept-tooltip');
    chip.addEventListener('mouseenter', () => {
      if (window.innerWidth > 768) tooltip.classList.add('show');
    });
    chip.addEventListener('mouseleave', () => {
      if (window.innerWidth > 768) tooltip.classList.remove('show');
    });
    chip.addEventListener('click', (e) => {
      if (window.innerWidth <= 768) {
        e.stopPropagation();
        document.querySelectorAll('.concept-tooltip.show').forEach((tip) => { if (tip !== tooltip) tip.classList.remove('show'); });
        tooltip.classList.toggle('show');
      } else {
        document.getElementById('studentQuestionInput').value = `What does ${chip.textContent.trim()} mean in Lesson 3.4?`;
        window.location.hash = 'ai-tools';
      }
    });
    tooltip.addEventListener('click', (e) => e.stopPropagation());
  });
  document.addEventListener('click', () => {
    document.querySelectorAll('.concept-tooltip.show').forEach((tip) => tip.classList.remove('show'));
  });
}

function bindPatternQuiz() {
  const map = Object.fromEntries(lesson34Patterns.map((item, idx) => [`q${idx + 1}`, item]));
  document.getElementById('patternGrid').addEventListener('click', async (e) => {
    const btn = e.target.closest('button');
    if (!btn) return;
    const qName = btn.dataset.q || btn.dataset.reset;
    if (!qName) return;
    const feedbackBox = document.getElementById(`feedback-${qName}`);

    if (btn.dataset.reset) {
      document.querySelectorAll(`input[name="${qName}"]`).forEach(input => { input.checked = false; });
      feedbackBox.className = 'mcq-feedback';
      feedbackBox.textContent = '';
      return;
    }

    const selected = document.querySelector(`input[name="${qName}"]:checked`);
    if (!selected) {
      feedbackBox.className = 'mcq-feedback show incorrect';
      feedbackBox.textContent = 'Please choose an answer first.';
      return;
    }

    const question = map[qName];
    const ok = selected.value === question.correct;
    feedbackBox.className = `mcq-feedback show ${ok ? 'correct' : 'incorrect'}`;
    feedbackBox.textContent = ok ? question.feedback.correct : question.feedback.incorrect;

    await window.logLessonEvent({
      type: 'pattern_quiz',
      course: window.APP_CONFIG.COURSE,
      lesson: window.APP_CONFIG.LESSON,
      payload: { question: qName, selected: selected.value, correct: question.correct, ok }
    });
  });
}

